import { Prisma, type Deadline, type PrismaClient } from '@prisma/client';
import type { CreateDeadlineRequest, UpdateDeadlineRequest } from '@charitypilot/shared';
import { AppError } from '../utils/errors.js';
import {
  civilDateFromPrisma,
  nullableCivilDateFromPrisma,
  prismaDateFromCivil,
} from '../utils/civil-date.js';
import {
  deriveIrishGovernanceDeadlines,
  type GeneratedDeadlineSpec,
} from './deadline-calendar.js';

type DeadlinePrisma = Pick<
  Prisma.TransactionClient,
  'deadline' | 'deadlineReminderLog' | 'organisation' | '$queryRaw'
>;

type DeadlineWithGeneration = Deadline & {
  profileRuleKey?: string | null;
};

function isUniqueConstraintError(error: unknown) {
  return Boolean(error && typeof error === 'object' && (error as { code?: unknown }).code === 'P2002');
}

export function publicDeadline(deadline: DeadlineWithGeneration) {
  return {
    ...deadline,
    dueDate: civilDateFromPrisma(deadline.dueDate),
    completedDate: deadline.completedDate?.toISOString() ?? null,
    supersededAt: deadline.supersededAt?.toISOString() ?? null,
    archivedAt: deadline.archivedAt?.toISOString() ?? null,
    createdAt: deadline.createdAt.toISOString(),
    updatedAt: deadline.updatedAt.toISOString(),
  };
}

function pageBounds(page: number, pageSize: number) {
  const safePage = Math.max(1, page);
  const safePageSize = Math.min(100, Math.max(1, pageSize));
  return { page: safePage, pageSize: safePageSize, skip: (safePage - 1) * safePageSize };
}

function normalizeReminderDays(values: number[]) {
  return [...new Set(values)].sort((left, right) => right - left);
}

function generationData(organisationId: string, spec: GeneratedDeadlineSpec, version: number) {
  return {
    organisationId,
    title: spec.title,
    description: spec.description,
    dueDate: prismaDateFromCivil(spec.dueDate),
    isAutoGenerated: true,
    generatedKind: spec.kind,
    generatedKey: spec.key,
    generationVersion: version,
    generationRuleVersion: spec.ruleVersion,
    generationFingerprint: spec.fingerprint,
    generationSource: {
      sources: spec.sources,
      professionalReviewRequired: spec.professionalReviewRequired,
      warnings: spec.warnings,
    } satisfies Prisma.InputJsonObject,
    generationInputs: spec.inputs as Prisma.InputJsonObject,
    isComplete: false,
    completedDate: null,
    completionDateKnown: true,
    reminderDays: [30, 14, 7],
  };
}

export class DeadlineService {
  constructor(private prisma: DeadlinePrisma | PrismaClient) {}

  async list(organisationId: string, page = 1, pageSize = 50) {
    const bounds = pageBounds(page, pageSize);
    const where = { organisationId, supersededAt: null, archivedAt: null };
    const [rows, total] = await Promise.all([
      this.prisma.deadline.findMany({
        where,
        orderBy: [{ isComplete: 'asc' }, { dueDate: 'asc' }, { id: 'asc' }],
        skip: bounds.skip,
        take: bounds.pageSize,
      }),
      this.prisma.deadline.count({ where }),
    ]);
    const data = rows.map((row) => publicDeadline(row as DeadlineWithGeneration));
    return {
      data,
      total,
      page: bounds.page,
      pageSize: bounds.pageSize,
      hasMore: bounds.skip + data.length < total,
    };
  }

  async history(organisationId: string, page = 1, pageSize = 50) {
    const bounds = pageBounds(page, pageSize);
    const where = {
      organisationId,
      isAutoGenerated: true,
      OR: [{ isComplete: true }, { supersededAt: { not: null } }],
    };
    const [rows, total] = await Promise.all([
      this.prisma.deadline.findMany({
        where,
        orderBy: [{ createdAt: 'desc' }, { id: 'desc' }],
        skip: bounds.skip,
        take: bounds.pageSize,
      }),
      this.prisma.deadline.count({ where }),
    ]);
    const data = rows.map((row) => publicDeadline(row as DeadlineWithGeneration));
    return {
      data,
      total,
      page: bounds.page,
      pageSize: bounds.pageSize,
      hasMore: bounds.skip + data.length < total,
    };
  }

  async reminderHistory(
    organisationId: string,
    page = 1,
    pageSize = 50,
    filters: { deadlineId?: string; status?: string } = {},
  ) {
    const bounds = pageBounds(page, pageSize);
    const where = {
      organisationId,
      ...(filters.deadlineId ? { deadlineId: filters.deadlineId } : {}),
      ...(filters.status ? { status: filters.status as never } : {}),
    };
    const [rows, total] = await Promise.all([
      this.prisma.deadlineReminderLog.findMany({
        where,
        orderBy: [{ reservedAt: 'desc' }, { id: 'desc' }],
        skip: bounds.skip,
        take: bounds.pageSize,
      }),
      this.prisma.deadlineReminderLog.count({ where }),
    ]);
    const data = rows.map((row) => ({
      id: row.id,
      deadlineId: row.deadlineId,
      deadlineTitle: row.deadlineTitle,
      deadlineDueDate: civilDateFromPrisma(row.deadlineDueDate),
      deadlineScheduleVersion: row.deadlineScheduleVersion,
      deadlineContextKind: row.deadlineSnapshotKnown
        ? 'RECORDED_AT_RESERVATION' as const
        : 'MIGRATION_TIME_CONTEXT' as const,
      deadlineSnapshotKnown: row.deadlineSnapshotKnown,
      deliveryTimingKnown: row.deliveryTimingKnown,
      legacyDeliveryStatus: row.legacyDeliveryStatus as 'SENT' | 'FAILED' | 'SKIPPED' | null,
      legacyRecordedAt: row.legacyRecordedAt?.toISOString() ?? null,
      email: row.email,
      reminderDays: row.reminderDays,
      status: row.status,
      error: row.error,
      reservedAt: row.deliveryTimingKnown ? row.reservedAt.toISOString() : null,
      attemptedAt: row.attemptedAt?.toISOString() ?? null,
      providerRequestStartedAt: row.providerRequestStartedAt?.toISOString() ?? null,
      reconciliationOutcome: row.reconciliationOutcome,
      reconciledAt: row.reconciledAt?.toISOString() ?? null,
      sentAt: row.sentAt?.toISOString() ?? null,
    }));
    return {
      data,
      total,
      page: bounds.page,
      pageSize: bounds.pageSize,
      hasMore: bounds.skip + data.length < total,
    };
  }

  async create(organisationId: string, data: CreateDeadlineRequest) {
    try {
      const deadline = await this.prisma.deadline.create({
        data: {
          organisationId,
          title: data.title,
          description: data.description,
          dueDate: prismaDateFromCivil(data.dueDate),
          reminderDays: normalizeReminderDays(data.reminderDays ?? [30, 14, 7]),
          ...('profileRuleKey' in data && data.profileRuleKey
            ? { profileRuleKey: data.profileRuleKey }
            : {}),
        },
      });
      return publicDeadline(deadline as DeadlineWithGeneration);
    } catch (error) {
      if (isUniqueConstraintError(error) && 'profileRuleKey' in data && data.profileRuleKey) {
        throw new AppError(409, 'PROFILE_REVIEW_ALREADY_SCHEDULED', 'That profile review already has a current deadline.');
      }
      throw error;
    }
  }

  async update(organisationId: string, id: string, data: UpdateDeadlineRequest) {
    const { expectedUpdatedAt, ...changes } = data;
    const expectedUpdatedAtInstant = new Date(expectedUpdatedAt);
    const deadline = await this.prisma.deadline.findFirst({
      where: { id, organisationId, archivedAt: null },
    });

    if (!deadline) {
      throw new AppError(404, 'DEADLINE_NOT_FOUND', 'Deadline not found');
    }
    if (deadline.supersededAt) {
      throw new AppError(409, 'DEADLINE_SUPERSEDED', 'This generated deadline is historical and cannot be changed.');
    }
    if (deadline.updatedAt.getTime() !== expectedUpdatedAtInstant.getTime()) {
      throw new AppError(
        409,
        'DEADLINE_UPDATE_CONFLICT',
        'This deadline changed since it was loaded. Refresh and review the latest values.',
      );
    }

    if (deadline.isAutoGenerated) {
      const changedFields = Object.entries(changes)
        .filter(([, value]) => value !== undefined)
        .map(([key]) => key);
      if (changedFields.length !== 1 || changedFields[0] !== 'isComplete' || data.isComplete !== true) {
        throw new AppError(
          409,
          'GENERATED_DEADLINE_IMMUTABLE',
          'Generated deadlines cannot be edited, reopened, rescheduled, or deleted.',
        );
      }

      const completed = await this.prisma.deadline.updateMany({
        where: {
          id,
          organisationId,
          archivedAt: null,
          supersededAt: null,
          isComplete: false,
          updatedAt: expectedUpdatedAtInstant,
        },
        data: { isComplete: true, completedDate: new Date(), completionDateKnown: true },
      });
      if (completed.count !== 1) {
        throw new AppError(409, 'DEADLINE_ALREADY_COMPLETED', 'This generated deadline is already complete or changed.');
      }
      const updated = await this.prisma.deadline.findUniqueOrThrow({ where: { id } });
      return publicDeadline(updated as DeadlineWithGeneration);
    }

    const nextReminderDays = changes.reminderDays
      ? normalizeReminderDays(changes.reminderDays)
      : undefined;
    const dueDateChanged = changes.dueDate !== undefined && changes.dueDate !== civilDateFromPrisma(deadline.dueDate);
    const reopened = changes.isComplete === false && deadline.isComplete;
    const newlyCompleted = changes.isComplete === true && !deadline.isComplete;
    const scheduleChanged = dueDateChanged || reopened;

    try {
      const updated = await this.prisma.deadline.updateMany({
        where: {
          id,
          organisationId,
          archivedAt: null,
          supersededAt: null,
          updatedAt: expectedUpdatedAtInstant,
        },
        data: {
          title: changes.title,
          description: changes.description,
          dueDate: changes.dueDate ? prismaDateFromCivil(changes.dueDate) : undefined,
          isComplete: changes.isComplete,
          completedDate: newlyCompleted ? new Date() : reopened ? null : undefined,
          completionDateKnown: newlyCompleted || reopened ? true : undefined,
          reminderDays: nextReminderDays,
          scheduleVersion: scheduleChanged ? { increment: 1 } : undefined,
        },
      });
      if (updated.count !== 1) {
        throw new AppError(
          409,
          'DEADLINE_UPDATE_CONFLICT',
          'This deadline changed while it was being saved. Refresh and try again.',
        );
      }
      const current = await this.prisma.deadline.findUniqueOrThrow({ where: { id } });
      return publicDeadline(current as DeadlineWithGeneration);
    } catch (error) {
      if (isUniqueConstraintError(error) && reopened && deadline.profileRuleKey) {
        throw new AppError(
          409,
          'PROFILE_REVIEW_ALREADY_SCHEDULED',
          'A newer current deadline already exists for that profile review.',
        );
      }
      throw error;
    }
  }

  async remove(organisationId: string, id: string, expectedUpdatedAt: string) {
    const expectedUpdatedAtInstant = new Date(expectedUpdatedAt);
    const deadline = await this.prisma.deadline.findFirst({
      where: { id, organisationId, archivedAt: null },
    });

    if (!deadline) {
      throw new AppError(404, 'DEADLINE_NOT_FOUND', 'Deadline not found');
    }
    if (deadline.isAutoGenerated) {
      throw new AppError(409, 'GENERATED_DEADLINE_IMMUTABLE', 'Generated deadlines cannot be deleted.');
    }
    if (deadline.updatedAt.getTime() !== expectedUpdatedAtInstant.getTime()) {
      throw new AppError(
        409,
        'DEADLINE_UPDATE_CONFLICT',
        'This deadline changed since it was loaded. Refresh before deleting it.',
      );
    }

    const archived = await this.prisma.deadline.updateMany({
      where: {
        id,
        organisationId,
        archivedAt: null,
        updatedAt: expectedUpdatedAtInstant,
      },
      data: { archivedAt: new Date() },
    });
    if (archived.count !== 1) {
      throw new AppError(
        409,
        'DEADLINE_UPDATE_CONFLICT',
        'This deadline changed while it was being deleted. Refresh and try again.',
      );
    }
  }

  async reconcileGeneratedDeadlines(organisationId: string) {
    await this.prisma.$queryRaw`SELECT "id" FROM "Organisation" WHERE "id" = ${organisationId} FOR UPDATE`;
    const organisation = await this.prisma.organisation.findUniqueOrThrow({
      where: { id: organisationId },
      select: {
        financialYearEnd: true,
        legalForm: true,
        legalFormConfirmedAt: true,
        incorporationDate: true,
        memberCount: true,
        lastActualAgmDate: true,
        lastUnanimousAnnualMemberResolutionDate: true,
        croAnnualReturnDate: true,
        croAnnualReturnDateConfirmedAt: true,
      },
    });

    const desired = deriveIrishGovernanceDeadlines({
      financialYearEnd: nullableCivilDateFromPrisma(organisation.financialYearEnd),
      legalForm: organisation.legalForm,
      legalFormConfirmedAt: organisation.legalFormConfirmedAt,
      incorporationDate: nullableCivilDateFromPrisma(organisation.incorporationDate),
      memberCount: organisation.memberCount,
      lastActualAgmDate: nullableCivilDateFromPrisma(organisation.lastActualAgmDate),
      lastUnanimousAnnualMemberResolutionDate: nullableCivilDateFromPrisma(
        organisation.lastUnanimousAnnualMemberResolutionDate,
      ),
      croAnnualReturnDate: nullableCivilDateFromPrisma(organisation.croAnnualReturnDate),
      croAnnualReturnDateConfirmedAt: organisation.croAnnualReturnDateConfirmedAt,
    });

    const allGenerated = await this.prisma.deadline.findMany({
      where: { organisationId, isAutoGenerated: true, generatedKey: { not: null } },
      orderBy: [{ generationVersion: 'asc' }, { createdAt: 'asc' }],
    });
    const currentByKey = new Map(
      allGenerated
        .filter((deadline) => deadline.supersededAt === null && deadline.archivedAt === null && deadline.generatedKey)
        .map((deadline) => [deadline.generatedKey!, deadline]),
    );
    const versionByKey = new Map<string, number>();
    for (const deadline of allGenerated) {
      if (!deadline.generatedKey || !deadline.generationVersion) continue;
      versionByKey.set(
        deadline.generatedKey,
        Math.max(versionByKey.get(deadline.generatedKey) ?? 0, deadline.generationVersion),
      );
    }

    const desiredByKey = new Map(desired.map((spec) => [spec.key, spec]));
    const now = new Date();

    for (const [key, current] of currentByKey) {
      const spec = desiredByKey.get(key);
      if (!spec) {
        await this.prisma.deadline.update({
          where: { id: current.id },
          data: { supersededAt: now, supersessionReason: 'INPUT_REMOVED' },
        });
        continue;
      }
      if (
        current.generationFingerprint === spec.fingerprint &&
        current.generationRuleVersion === spec.ruleVersion &&
        civilDateFromPrisma(current.dueDate) === spec.dueDate
      ) {
        desiredByKey.delete(key);
        continue;
      }

      const reason = current.generationRuleVersion !== spec.ruleVersion
        ? 'RULE_CHANGED'
        : current.isComplete
          ? 'RECURRENCE_ADVANCED'
          : 'INPUT_CHANGED';
      await this.prisma.deadline.update({
        where: { id: current.id },
        data: { supersededAt: now, supersessionReason: reason },
      });
      const version = (versionByKey.get(key) ?? 0) + 1;
      const successor = await this.prisma.deadline.create({
        data: generationData(organisationId, spec, version),
      });
      await this.prisma.deadline.update({
        where: { id: current.id },
        data: { supersededById: successor.id },
      });
      versionByKey.set(key, version);
      desiredByKey.delete(key);
    }

    for (const spec of desiredByKey.values()) {
      const version = (versionByKey.get(spec.key) ?? 0) + 1;
      await this.prisma.deadline.create({
        data: generationData(organisationId, spec, version),
      });
      versionByKey.set(spec.key, version);
    }
  }
}
