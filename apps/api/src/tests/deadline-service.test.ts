const EXPECTED_UPDATED_AT = '2026-01-01T00:00:00.000Z';

type Call = { name: string; args: any };

function deadline(overrides: Record<string, unknown> = {}) {
  return {
    id: 'deadline-1',
    organisationId: 'org-1',
    title: 'Board review',
    description: null,
    dueDate: new Date('2026-06-30T00:00:00.000Z'),
    isAutoGenerated: false,
    scheduleVersion: 1,
    profileRuleKey: null,
    generatedKind: null,
    generatedKey: null,
    generationVersion: null,
    generationRuleVersion: null,
    generationFingerprint: null,
    generationSource: null,
    generationInputs: null,
    isComplete: false,
    completedDate: null,
    completionDateKnown: true,
    reminderDays: [30, 14, 7],
    supersededAt: null,
    supersededById: null,
    supersessionReason: null,
    archivedAt: null,
    createdAt: new Date('2026-01-01T00:00:00.000Z'),
    updatedAt: new Date('2026-01-01T00:00:00.000Z'),
    ...overrides,
  };
}

function organisation(overrides: Record<string, unknown> = {}) {
  return {
    financialYearEnd: null,
    legalForm: null,
    legalFormConfirmedAt: null,
    incorporationDate: null,
    memberCount: null,
    lastActualAgmDate: null,
    lastUnanimousAnnualMemberResolutionDate: null,
    croAnnualReturnDate: null,
    croAnnualReturnDateConfirmedAt: null,
    ...overrides,
  };
}

function buildService(options: {
  found?: ReturnType<typeof deadline> | null;
  listRows?: ReturnType<typeof deadline>[];
  generatedRows?: ReturnType<typeof deadline>[];
  reminderRows?: Array<Record<string, any>>;
  organisation?: ReturnType<typeof organisation>;
  updateManyCount?: number;
  updateError?: unknown;
} = {}) {
  const calls: Call[] = [];
  const found = options.found === undefined ? deadline() : options.found;
  const prisma = {
    $queryRaw: async (...args: unknown[]) => {
      calls.push({ name: '$queryRaw', args });
      return [{ id: 'org-1' }];
    },
    organisation: {
      findUniqueOrThrow: async (args: unknown) => {
        calls.push({ name: 'organisation.findUniqueOrThrow', args });
        return options.organisation ?? organisation();
      },
    },
    deadline: {
      findMany: async (args: any) => {
        calls.push({ name: 'deadline.findMany', args });
        return args.where?.isAutoGenerated ? options.generatedRows ?? [] : options.listRows ?? [];
      },
      count: async (args: unknown) => {
        calls.push({ name: 'deadline.count', args });
        return options.listRows?.length ?? options.generatedRows?.length ?? 0;
      },
      findFirst: async (args: unknown) => {
        calls.push({ name: 'deadline.findFirst', args });
        return found;
      },
      findUniqueOrThrow: async (args: unknown) => {
        calls.push({ name: 'deadline.findUniqueOrThrow', args });
        return deadline({ ...found, isComplete: true, completedDate: new Date('2026-07-10T00:00:00.000Z') });
      },
      create: async (args: any) => {
        calls.push({ name: 'deadline.create', args });
        return deadline({ id: `created-${calls.length}`, ...args.data });
      },
      update: async (args: any) => {
        calls.push({ name: 'deadline.update', args });
        if (options.updateError) throw options.updateError;
        const definedData = Object.fromEntries(
          Object.entries(args.data).filter(([, value]) => value !== undefined),
        );
        return deadline({ id: args.where.id, ...found, ...definedData });
      },
      updateMany: async (args: any) => {
        calls.push({ name: 'deadline.updateMany', args });
        if (options.updateError) throw options.updateError;
        return { count: options.updateManyCount ?? 1 };
      },
    },
    deadlineReminderLog: {
      findMany: async (args: unknown) => {
        calls.push({ name: 'deadlineReminderLog.findMany', args });
        return options.reminderRows ?? [];
      },
      count: async (args: unknown) => {
        calls.push({ name: 'deadlineReminderLog.count', args });
        return options.reminderRows?.length ?? 0;
      },
    },
  };
  return { service: new DeadlineService(prisma as never), calls };
}

test('list returns current non-archived rows with exact civil dates', async () => {
  const row = deadline();
  const { service, calls } = buildService({ listRows: [row] });
  const result = await service.list('org-1');

  assert.equal(result.data[0].dueDate, '2026-06-30');
  assert.deepEqual(calls.find((call) => call.name === 'deadline.findMany')?.args.where, {
    organisationId: 'org-1',
    supersededAt: null,
    archivedAt: null,
  });
});

